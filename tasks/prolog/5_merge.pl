%$ определить предикат mrg(L1, L2, R) который для двух отсортированных списков L1 и L2 
% определяет список R, составленный из этих элементов

mrg(L1, [], L1).
mrg([], L2, L2).
mrg([HL1|TL1], [HL2|TL2], R) :- HL1 >= HL2, mrg(TL2, [HL1|TL1], T), R = [HL2|T].
mrg([HL1|TL1], [HL2|TL2], R) :- HL1  < HL2, mrg(TL1, [HL2|TL2], T), R = [HL1|T].

% Результаты:
%
% ?- mrg([1, 2, 5], [3, 4, 6], R).
% R = [1, 2, 3, 4, 5, 6] .
%
% ?- mrg([1, 3, 5], [2, 6, 8], R).
% R = [1, 2, 3, 5, 6, 8] .

