% определить предикат mrg(L1, L2, R) который для двух отсортированных списков L1 и L2 
% определяет список R, составленный из этих элементов



mrg([L | LT], [R | RT], M) :-  L < R, append([L], M1, M), mrg(LT, [R| RT], M1).
mrg([L | LT], [R | RT], M) :-  L >= R, append([R], M1, M), mrg([L| LT], RT, M1).
mrg([], R, M) :- M = R.
mrg(L, [], M) :- M = L.

'
?- mrg([3, 5, 5, 5, 5, 8], [4, 6, 6, 7], M).
M = [3, 4, 5, 5, 5, 5, 6, 6, 7|...] ;
false.

?- mrg([3, 5], [4], M).
M = [3, 4, 5] 
'
